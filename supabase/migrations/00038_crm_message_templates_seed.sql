-- ═══ Add RPC for incrementing campaign stats ═══
CREATE OR REPLACE FUNCTION increment_campaign_stat(
  p_campaign_id UUID,
  p_stat_key TEXT
) RETURNS void AS $$
BEGIN
  UPDATE crm_campaigns
  SET stats = jsonb_set(
    COALESCE(stats, '{}'::jsonb),
    ARRAY[p_stat_key],
    (COALESCE((stats->>p_stat_key)::integer, 0) + 1)::text::jsonb
  )
  WHERE id = p_campaign_id;
END;
$$ LANGUAGE plpgsql;
